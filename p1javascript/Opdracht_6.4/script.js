// Oefening 1: Maak een optel-functie genaamd calculate
function calculate(a, b) {
    // Bereken de som van de twee getallen
    const som = a + b;
    // Toon het resultaat in de console
    console.log("Antwoord:", som);
}

// Roep de functie aan met twee random getallen
calculate(1, 12);  // Voorbeeld: dit zal "Antwoord: 13" in de console tonen


// Oefening 2: Maak een functie genaamd multiplyByFive
function multiplyByFive(number) {
    // Vermenigvuldig het getal met 5
    const resultaat = number * 5;
    // Toon het resultaat in de console
    console.log("Antwoord:", resultaat);
}

// Roep de functie aan met een random getal
multiplyByFive(7);  // Voorbeeld: dit zal "Antwoord: 35" in de console tonen


// Oefening 3: Maak een functie genaamd calculateMinutes
function calculateMinutes(seconds) {
    // Zet seconden om naar minuten
    const minuten = seconds / 60;
    // Toon het resultaat in de console
    console.log("Antwoord:", minuten);
}

// Roep de functie aan met een aantal seconden
calculateMinutes(3600);  // Voorbeeld: dit zal "Antwoord: 60" in de console tonen
